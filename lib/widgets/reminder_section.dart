import 'package:flutter/material.dart';
import '../models/task.dart';
import '../utils/app_theme.dart';

// ─────────────────────────────────────────────────────────────
// ReminderSection — self-contained collapsible widget
// Used in both AddTaskScreen and EditTaskScreen.
// Caller passes isSingleDay to show the right mode options.
// ─────────────────────────────────────────────────────────────

class ReminderSection extends StatefulWidget {
  final bool isSingleDay;
  final ReminderMode initialMode;
  final TimeOfDay initialTime;
  final int initialCustomDays;
  final ValueChanged<ReminderConfig> onChanged;

  const ReminderSection({
    super.key,
    required this.isSingleDay,
    required this.onChanged,
    this.initialMode = ReminderMode.none,
    this.initialTime = const TimeOfDay(hour: 9, minute: 0),
    this.initialCustomDays = 1,
  });

  @override
  State<ReminderSection> createState() => _ReminderSectionState();
}

class ReminderConfig {
  final ReminderMode mode;
  final TimeOfDay time;
  final int customDays;
  ReminderConfig({
    required this.mode,
    required this.time,
    required this.customDays,
  });
}

class _ReminderSectionState extends State<ReminderSection>
    with SingleTickerProviderStateMixin {
  late AnimationController _controller;
  late Animation<double> _expandAnim;

  bool _enabled = false;
  late ReminderMode _mode;
  late TimeOfDay _time;
  late int _customDays;

  @override
  void initState() {
    super.initState();
    _mode = widget.initialMode;
    _time = widget.initialTime;
    _customDays = widget.initialCustomDays;
    _enabled = _mode != ReminderMode.none;

    _controller = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 320),
    );
    _expandAnim = CurvedAnimation(
      parent: _controller,
      curve: Curves.easeInOutCubic,
    );
    if (_enabled) _controller.value = 1.0;
  }

  @override
  void didUpdateWidget(ReminderSection old) {
    super.didUpdateWidget(old);
    // When date changes make the single/multi switch reset mode if needed
    if (old.isSingleDay != widget.isSingleDay) {
      _resetModeForDuration();
    }
  }

  void _resetModeForDuration() {
    if (widget.isSingleDay &&
        (_mode == ReminderMode.onceDayBefore ||
            _mode == ReminderMode.daily ||
            _mode == ReminderMode.customDays)) {
      setState(() => _mode = ReminderMode.onDueDate);
      _emit();
    } else if (!widget.isSingleDay && _mode == ReminderMode.onDueDate) {
      setState(() => _mode = ReminderMode.onceDayBefore);
      _emit();
    }
  }

  @override
  void dispose() {
    _controller.dispose();
    super.dispose();
  }

  void _toggle(bool val) {
    setState(() {
      _enabled = val;
      if (_enabled) {
        // Pick sensible default for current duration type
        _mode = widget.isSingleDay
            ? ReminderMode.onDueDate
            : ReminderMode.onceDayBefore;
        _controller.forward();
      } else {
        _mode = ReminderMode.none;
        _controller.reverse();
      }
    });
    _emit();
  }

  void _emit() {
    widget.onChanged(
      ReminderConfig(
        mode: _enabled ? _mode : ReminderMode.none,
        time: _time,
        customDays: _customDays,
      ),
    );
  }

  Future<void> _pickTime() async {
    final picked = await showTimePicker(
      context: context,
      initialTime: _time,
      builder: (ctx, child) => MediaQuery(
        data: MediaQuery.of(ctx).copyWith(alwaysUse24HourFormat: false),
        child: child!,
      ),
    );
    if (picked != null) {
      setState(() => _time = picked);
      _emit();
    }
  }

  String _formatTime(TimeOfDay t) {
    final h = t.hourOfPeriod == 0 ? 12 : t.hourOfPeriod;
    final m = t.minute.toString().padLeft(2, '0');
    final period = t.period == DayPeriod.am ? 'AM' : 'PM';
    return '$h:$m $period';
  }

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    final isDark = theme.brightness == Brightness.dark;

    return Container(
      decoration: BoxDecoration(
        color: isDark ? AppColors.darkSurface : AppColors.lightSurface,
        borderRadius: BorderRadius.circular(AppSizes.radiusButton),
        border: Border.all(
          color: _enabled
              ? theme.colorScheme.primary.withValues(alpha: 0.35)
              : theme.dividerColor,
          width: _enabled ? 1.5 : 1,
        ),
      ),
      child: Column(
        mainAxisSize: MainAxisSize.min,
        children: [
          // ── Header row: icon + label + toggle ──
          Padding(
            padding: const EdgeInsets.symmetric(
              horizontal: AppSizes.spacingL,
              vertical: AppSizes.spacingM + 2,
            ),
            child: Row(
              children: [
                Container(
                  width: 34,
                  height: 34,
                  decoration: BoxDecoration(
                    color: _enabled
                        ? theme.colorScheme.primary.withValues(alpha: 0.1)
                        : theme.dividerColor.withValues(alpha: 0.5),
                    borderRadius: BorderRadius.circular(AppSizes.radiusSmall),
                  ),
                  child: Icon(
                    Icons.notifications_rounded,
                    size: 18,
                    color: _enabled
                        ? theme.colorScheme.primary
                        : theme.textTheme.labelSmall?.color,
                  ),
                ),
                const SizedBox(width: AppSizes.spacingM),
                Expanded(
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Text(
                        'Reminder',
                        style: theme.textTheme.titleMedium?.copyWith(
                          fontSize: AppSizes.fontBody + 1,
                        ),
                      ),
                      if (!_enabled)
                        Text(
                          'Tap to set a reminder',
                          style: theme.textTheme.labelSmall,
                        ),
                      if (_enabled)
                        Text(
                          _summaryText(),
                          style: theme.textTheme.labelSmall?.copyWith(
                            color: theme.colorScheme.primary.withValues(
                              alpha: 0.8,
                            ),
                          ),
                        ),
                    ],
                  ),
                ),
                Switch.adaptive(
                  value: _enabled,
                  onChanged: _toggle,
                  activeThumbColor: theme.colorScheme.primary,
                ),
              ],
            ),
          ),

          // ── Expandable body ──
          SizeTransition(
            sizeFactor: _expandAnim,
            child: Column(
              mainAxisSize: MainAxisSize.min,
              children: [
                Divider(height: 1, color: theme.dividerColor),
                Padding(
                  padding: const EdgeInsets.fromLTRB(
                    AppSizes.spacingL,
                    AppSizes.spacingM,
                    AppSizes.spacingL,
                    AppSizes.spacingL,
                  ),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    mainAxisSize: MainAxisSize.min,
                    children: [
                      // ── Mode selector ──
                      Text(
                        'WHEN TO REMIND',
                        style: theme.textTheme.labelMedium,
                      ),
                      const SizedBox(height: AppSizes.spacingS),
                      _modeSelector(theme),
                      // ── Custom days input (only for customDays mode) ──
                      if (_mode == ReminderMode.customDays) ...[
                        const SizedBox(height: AppSizes.spacingM),
                        _customDaysRow(theme),
                      ],
                      const SizedBox(height: AppSizes.spacingL),
                      // ── Time picker ──
                      Text('AT WHAT TIME', style: theme.textTheme.labelMedium),
                      const SizedBox(height: AppSizes.spacingS),
                      _timeTile(theme),
                    ],
                  ),
                ),
              ],
            ),
          ),
        ],
      ),
    );
  }

  // ── Mode chips ────────────────────────────────────────────
  Widget _modeSelector(ThemeData theme) {
    final modes = widget.isSingleDay
        ? [
            _ModeOption(
              mode: ReminderMode.onDueDate,
              label: 'On due day',
              icon: Icons.today_rounded,
            ),
          ]
        : [
            _ModeOption(
              mode: ReminderMode.onceDayBefore,
              label: 'Day before',
              icon: Icons.event_rounded,
            ),
            _ModeOption(
              mode: ReminderMode.daily,
              label: 'Daily',
              icon: Icons.repeat_rounded,
            ),
            _ModeOption(
              mode: ReminderMode.customDays,
              label: 'Custom',
              icon: Icons.tune_rounded,
            ),
          ];

    return Wrap(
      spacing: AppSizes.spacingS,
      runSpacing: AppSizes.spacingS,
      children: modes.map((opt) => _modeChip(theme, opt)).toList(),
    );
  }

  Widget _modeChip(ThemeData theme, _ModeOption opt) {
    final isSelected = _mode == opt.mode;
    return GestureDetector(
      onTap: () {
        setState(() => _mode = opt.mode);
        _emit();
      },
      child: AnimatedContainer(
        duration: const Duration(milliseconds: 180),
        padding: const EdgeInsets.symmetric(
          horizontal: AppSizes.spacingM,
          vertical: AppSizes.spacingS + 1,
        ),
        decoration: BoxDecoration(
          color: isSelected
              ? theme.colorScheme.primary
              : theme.dividerColor.withValues(alpha: 0.4),
          borderRadius: BorderRadius.circular(AppSizes.radiusButton),
        ),
        child: Row(
          mainAxisSize: MainAxisSize.min,
          children: [
            Icon(
              opt.icon,
              size: 14,
              color: isSelected
                  ? (theme.brightness == Brightness.dark
                        ? AppColors.darkBg
                        : Colors.white)
                  : theme.textTheme.labelSmall?.color,
            ),
            const SizedBox(width: 5),
            Text(
              opt.label,
              style: TextStyle(
                fontSize: AppSizes.fontLabel,
                fontWeight: FontWeight.w700,
                color: isSelected
                    ? (theme.brightness == Brightness.dark
                          ? AppColors.darkBg
                          : Colors.white)
                    : theme.textTheme.labelSmall?.color,
              ),
            ),
          ],
        ),
      ),
    );
  }

  // ── Custom days stepper ───────────────────────────────────
  Widget _customDaysRow(ThemeData theme) {
    return Row(
      children: [
        Text('Days before due:', style: theme.textTheme.bodyMedium),
        const Spacer(),
        _stepperButton(
          icon: Icons.remove_rounded,
          onTap: () {
            if (_customDays > 1) {
              setState(() => _customDays--);
              _emit();
            }
          },
          theme: theme,
        ),
        Padding(
          padding: const EdgeInsets.symmetric(horizontal: AppSizes.spacingM),
          child: Text(
            '$_customDays',
            style: theme.textTheme.titleMedium?.copyWith(
              fontSize: 18,
              fontWeight: FontWeight.w800,
            ),
          ),
        ),
        _stepperButton(
          icon: Icons.add_rounded,
          onTap: () {
            if (_customDays < 30) {
              setState(() => _customDays++);
              _emit();
            }
          },
          theme: theme,
        ),
      ],
    );
  }

  Widget _stepperButton({
    required IconData icon,
    required VoidCallback onTap,
    required ThemeData theme,
  }) {
    return GestureDetector(
      onTap: onTap,
      child: Container(
        width: 34,
        height: 34,
        decoration: BoxDecoration(
          color: theme.dividerColor.withValues(alpha: 0.5),
          borderRadius: BorderRadius.circular(AppSizes.radiusSmall),
        ),
        child: Icon(icon, size: 18, color: theme.colorScheme.primary),
      ),
    );
  }

  // ── Time tile ─────────────────────────────────────────────
  Widget _timeTile(ThemeData theme) {
    final isDark = theme.brightness == Brightness.dark;
    return GestureDetector(
      onTap: _pickTime,
      child: Container(
        padding: const EdgeInsets.symmetric(
          horizontal: AppSizes.spacingL,
          vertical: AppSizes.spacingM,
        ),
        decoration: BoxDecoration(
          color: isDark
              ? Colors.white.withValues(alpha: 0.05)
              : Colors.black.withValues(alpha: 0.04),
          borderRadius: BorderRadius.circular(AppSizes.radiusSmall + 2),
          border: Border.all(color: theme.dividerColor),
        ),
        child: Row(
          children: [
            Icon(
              Icons.access_time_rounded,
              size: AppSizes.iconS,
              color: theme.colorScheme.primary,
            ),
            const SizedBox(width: AppSizes.spacingS),
            Text(_formatTime(_time), style: theme.textTheme.titleMedium),
            const Spacer(),
            Text('Tap to change', style: theme.textTheme.labelSmall),
            const SizedBox(width: AppSizes.spacingXS),
            Icon(
              Icons.chevron_right_rounded,
              size: AppSizes.iconM,
              color: theme.textTheme.labelSmall?.color,
            ),
          ],
        ),
      ),
    );
  }

  // ── Summary text shown in collapsed header ─────────────────
  String _summaryText() {
    final timeStr = _formatTime(_time);
    switch (_mode) {
      case ReminderMode.onDueDate:
        return 'On due day at $timeStr';
      case ReminderMode.onceDayBefore:
        return '1 day before at $timeStr';
      case ReminderMode.daily:
        return 'Daily at $timeStr';
      case ReminderMode.customDays:
        return '$_customDays day${_customDays > 1 ? "s" : ""} before at $timeStr';
      case ReminderMode.none:
        return 'Off';
    }
  }
}

class _ModeOption {
  final ReminderMode mode;
  final String label;
  final IconData icon;
  const _ModeOption({
    required this.mode,
    required this.label,
    required this.icon,
  });
}
